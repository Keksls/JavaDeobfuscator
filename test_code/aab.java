package aaa;

public class aab {
    public int caa(int a, int b) {
        return a + b;
    }

    public int cab(int a, int b) {
        return a - b;
    }

    public int cac(int a) {
        int b = 0;
        while (cad(b, b) < a) {
            b++;
        }
        return b;
    }

    public int cad(int a, int b) {
        return a * b;
    }
}